    name: character.name,
    shortName: character.shortName,
    textures: character.textures,
    spawn,
    tint: character.tint,
    accent: character.accent,
    speed: character.speed,
    jumpVelocity: character.jumpVelocity,
    airControl: character.airControl,
    weight: character.weight,
